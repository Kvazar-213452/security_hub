func main() {
	version := main_com.Get_version()
	version_conf := 1

	if version == version_conf {
		cmd := exec.Command("./app_back_end/head.exe")
		cmd.Run()
		fmt.Println("File executed successfully")
	} else {
		// port := main_com.FindFreePort()
		port := 52352

		http.Handle("/", http.FileServer(http.Dir("./html_tmp_update")))

		// Post
		http.HandleFunc("/current_ver", main_com.Post_get_current_ver)
		http.HandleFunc("/server_ver", main_com.Post_get_server_ver)
		http.HandleFunc("/url_desc", main_com.Post_get_url_desc)
		http.HandleFunc("/close", main_com.Post_close)
		http.HandleFunc("/updata_app", main_com.Post_updata_app)

		url := fmt.Sprintf("http://localhost:%d/index.html", port)
		browser.OpenURL(url)

		fmt.Printf("Server running on port %d...\n", port)
		http.ListenAndServe(fmt.Sprintf(":%d", port), nil)
	}
}
